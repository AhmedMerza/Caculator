    public void setAll(int b){
        
        int a = b;
        if (RDDEC.isSelected()){
        if (txtScreen1.getText().equals("")){
            setScreen(txtScreen1,String.valueOf(a));
            txtScreenBIN.setText(Integer.toString(a, 2));
            txtScreenOCT.setText(Integer.toString(a, 8));
            txtScreenHEX.setText(Integer.toString(a, 16).toUpperCase());
        }
        else{
            String c = txtScreen1.getText()+String.valueOf(a);
            //a += Integer.parseInt(txtScreen1.getText());
             txtScreen1.setText(c);
             txtScreenBIN.setText(Integer.toString(Integer.parseInt(c), 2));
             txtScreenOCT.setText(Integer.toString(Integer.parseInt(c), 8));
             txtScreenHEX.setText(Integer.toString(Integer.parseInt(c), 16).toUpperCase());
        }
        }
        else if (RDBIN.isSelected()){
                setScreen(txtScreenBIN,String.valueOf(a));
                int e = 0;
                String c = txtScreenBIN.getText();
                int r = 0;
                for(int i = c.length()-1;i>=0;i--){
                    if (c.charAt(i)=='1'){
                        e =e+(int)Math.pow(2, r);
                    }
                    r+=1;
                }
                txtScreen1.setText(String.valueOf(e));
                txtScreenHEX.setText(Integer.toString(e, 16).toUpperCase());
                txtScreenOCT.setText(Integer.toString(e, 8)); 
        }
        else if (RDOCT.isSelected()){
            setScreen(txtScreenOCT,String.valueOf(a));
            int e = 0, total = 0;
                String d = "";
                String c = txtScreenOCT.getText();
                int r = 0;
                for(int i = c.length()-1;i>=0;i--){
                        e = (int)Math.pow(8, r);
                        int u = Integer.parseInt(String.valueOf(c.charAt(i)));
                        u *= e;
                        total +=u;
                        r+=1;
                }
             txtScreen1.setText(String.valueOf(total));
             txtScreenBIN.setText(Integer.toString(total, 2));
             txtScreenHEX.setText(Integer.toString(total, 16).toUpperCase());
             txtScreenOCT.setText(Integer.toString(total, 8)); 
        }
        else{
            String he ;
            if (a==10)
               he = "A";
               else if (a==11)
                       he = "B";
                       else if (a==12)
                       he = "C";
                       else if (a==13)
                       he = "D";
                       else if (a==14)
                       he = "E";
                       else if (a==15)
                       he = "F";
                       else he = String.valueOf(a);
            
            setScreen(txtScreenHEX, he);
                int e = 0, total = 0;
                String d = "";
                String c = txtScreenHEX.getText();
                int r = 0;
                for(int i = c.length()-1;i>=0;i--){
                        e = (int)Math.pow(16, r);
                        if (c.charAt(i)=='A')
                            he = "10";
                        else if (c.charAt(i)=='B')
                       he = "11";
                       else if (c.charAt(i)=='C')
                       he = "12";
                       else if (c.charAt(i)=='D')
                       he = "13";
                       else if (c.charAt(i)=='E')
                       he = "14";
                       else if (c.charAt(i)=='F')
                       he = "15";
                       else he = String.valueOf(c.charAt(i));
                        
                        int u = Integer.parseInt(he);
                        u *= e;
                        total +=u;
                        r+=1;
                }
                txtScreen1.setText(String.valueOf(total));
                txtScreenBIN.setText(Integer.toString(total, 2));
                txtScreenHEX.setText(Integer.toString(total, 16).toUpperCase());
                txtScreenOCT.setText(Integer.toString(total, 8)); 
        }
    }
    
    
